    public final void getTenDV(){
        List<String> lst = controller.getAllDV();
        for(int i = 0 ; i < lst.size() ; i++){
            cbDichVu.addItem(lst.get(i));
        }
    }
    
    public final void showDonGia(String hd){
        clearData();
        List<DonGiaDV> lst = controller.getAll(hd);
        for(int i = 0 ; i < lst.size() ; i++){
            Object[] row = {
                lst.get(i).getDichVu(),
                lst.get(i).getGia()
            };
            tableModel.addRow(row);
        }
    }
    
    public final void clearData() {
        int n = tableModel.getRowCount() - 1;
        for (int i = n; i >= 0; i--) {
            tableModel.removeRow(i);
        }
    }

