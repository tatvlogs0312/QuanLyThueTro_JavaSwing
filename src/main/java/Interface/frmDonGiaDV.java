    private void btnTaoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnTaoActionPerformed
        // TODO add your handling code here:
        UUID rd = UUID.randomUUID();
        String id = String.valueOf(rd);
        String dv = (String)cbDichVu.getSelectedItem();
        int donGia = Integer.parseInt(txtDonGia.getText());
        String madv = controller.getMaDV(dv);
        DonGiaDV dgdv = new DonGiaDV(id, hd, madv, donGia);
        String check = controller.check(hd, madv);
        if(check.equals("")){
            boolean them = controller.insertDonGia(dgdv);
            if(them){
                JOptionPane.showMessageDialog(rootPane, "Them thanh cong");
            }
        }
        else{
            boolean sua = controller.updateDonGia(controller.getMaDonGia(hd, madv), donGia);
            if(sua){
                JOptionPane.showMessageDialog(rootPane, "Sua thanh cong");
            }
        }
        showDonGia(hd);
        txtDonGia.setText("");
    }//GEN-LAST:event_btnTaoActionPerformed

